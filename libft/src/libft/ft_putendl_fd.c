#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	ft_putstr_fd(s, fd);
	write(fd, "\n", 1);
}

/*
Envía la string ’s’ al
seguido de un salto de linea
----
s: La string a enviar. fd: El file descriptor
*/